#manipulando string

curso =  "fundamentos de python";
#pegando letra como vetor
print(curso[0])

#vetor de tras para frente
print(curso[-1]) 

# se colocar : dentro do vetor pega do inicio até a posição que você inicar 
print(curso[:11])

# faz a mesma coisa só que de tras para frente
print(curso[-5:])

#misto
print(curso[6:-9])
#negativo
print(curso[-10:-3])